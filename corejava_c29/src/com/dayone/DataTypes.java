package com.dayone;

public class DataTypes {
	
	public static void main(String args[]) {
		// Primitive - Store single data
		// Non primitive - Store multiple data 

		//Data Types
		int a  = 10;
		float b = 10.101f;
		double c = 33.5;
		boolean d = true;
		char e = 'a';
		
		// Print the Variable
		System.out.println("Integer : " + a);
		System.out.println("Float : " + b);
		System.out.println("Double : " + c);
		System.out.println("Boolean : " + d);
		System.out.println("Character : " + e + "\nEnd");
			
		}

}
